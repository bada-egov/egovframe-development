public class NewBatchWebProjectContext extends NewBatchProjectContext {

    /** 서블릿 버젼 */
    private String servletVersion;
    
    /** 서버 런타임 명 */
    private String runtimeName;
    
	/**
	 * servletVersion의 값을 가져온다
	 *
	 * @return the servletVersion
	 */
	public String getServletVersion() {
		return servletVersion;
	}
	/**
	 * servletVersion의 값을 설정한다.
	 *
	 * @param servletVersion the servletVersion to set
	 */
	public void setServletVersion(String servletVersion) {
		this.servletVersion = servletVersion;
	}
	/**
	 * runtimeName의 값을 가져온다
	 *
	 * @return the runtimeName
	 */
	public String getRuntimeName() {
		return runtimeName;
	}
	/**
	 * runtimeName의 값을 설정한다.
	 *
	 * @param runtimeName the runtimeName to set
	 */
	public void setRuntimeName(String runtimeName) {
		this.runtimeName = runtimeName;
	}

    
}
