package egovframework.dev.imp.commngt.util;

import org.w3c.dom.Node;

public class ComResourceUtilVO {
	
	/** web.xml에서 사용되는 URL 패턴*/
	private String urlPattern;
	/** DispatcherServlet내에서 정의되어있는 jsp의 prefix 패턴*/
	private String prefixPattern;
	/** 파일이 위치하는 url*/
	private String urlLocation;
	/** 파일이 위치하는 url을 정제한 데이*/
	private String[] locationPattern;
	/** 파일 내에서  Bean 존재여부*/
	private boolean existBean;
	/** 기존시스템의 root노드 */
	private Node asIsRootNode;
	/** 프로퍼티명 */
	private String propertyName;
	/** 프로퍼티 */
	private String property;
	/** 노드의 xpath*/
	private String nodeXpath;
	/** context urlLocation*/
	private String contextUrlLocation;
	
	
	public String getContextUrlLocation() {
		return contextUrlLocation;
	}
	public void setContextUrlLocation(String contextUrlLocation) {
		this.contextUrlLocation = contextUrlLocation;
	}
	public String getUrlLocation() {
		return urlLocation;
	}
	public void setUrlLocation(String urlLocation) {
		this.urlLocation = urlLocation;
	}
	public String[] getLocationPattern() {
		return locationPattern;
	}
	public void setLocationPattern(String[] locationPattern) {
		this.locationPattern = locationPattern;
	}
	public String getNodeXpath() {
		return nodeXpath;
	}
	public void setNodeXpath(String nodeXpath) {
		this.nodeXpath = nodeXpath;
	}
	public String getPropertyName() {
		return propertyName;
	}
	public void setPropertyName(String propertyName) {
		this.propertyName = propertyName;
	}
	public String getProperty() {
		return property;
	}
	public void setProperty(String property) {
		this.property = property;
	}
	public Node getAsIsRootNode() {
		return asIsRootNode;
	}
	public void setAsIsRootNode(Node asIsRootNode) {
		this.asIsRootNode = asIsRootNode;
	}
	public boolean isExistBean() {
		return existBean;
	}
	public void setExistBean(boolean existBean) {
		this.existBean = existBean;
	}
	public String getUrlPattern() {
		return urlPattern;
	}
	public void setUrlPattern(String urlPattern) {
		this.urlPattern = urlPattern;
	}
	public String getPrefixPattern() {
		return prefixPattern;
	}
	public void setPrefixPattern(String prefixPattern) {
		this.prefixPattern = prefixPattern;
	}
	
}
